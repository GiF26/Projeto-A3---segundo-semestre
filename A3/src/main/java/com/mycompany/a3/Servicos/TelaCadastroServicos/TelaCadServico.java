    public void clickBtnConfirmar(){

        btnConfirmar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                
                Servico s = new Servico();
                s.setNomeServico(txtNomeServico.getText());
               
                if(cmbCategoria.getItemAt(cmbCategoria.getSelectedIndex()).equals("Outro")){
                    s.setCategoria(0);
                }else{
                    s.setCategoria(cmbCategoria.getSelectedIndex());    
                }
                
                try {
                    if (s.salvar()) {
                        JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                        dispose();
                        screenMain.carregaCmbServico();
                        
                    }
                } catch (SQLException ex) {
                    System.getLogger(TelaCadServico.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                }
            }
        });
    }

    public void clickBtnFechar(){
        btnCancelar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                dispose();
            }
        });
    }
    
    public void carregaComboCategoria(){
        cmbCategoria.removeAllItems(); // Limpa itens existentes
        
        String sql = "SELECT ID, NOME_CATEGORIA FROM CATEGORIAS ORDER BY ID";
        
        try (Connection conn = configConexao.getConexao();
         PreparedStatement stmt = conn.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {
        
        while (rs.next()) {
            cmbCategoria.addItem(rs.getString("ID") + " - " + rs.getString("NOME_CATEGORIA"));
        }
        cmbCategoria.addItem("Outro");
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, 
                "Erro ao carregar dados: " + e.getMessage(),
                "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
